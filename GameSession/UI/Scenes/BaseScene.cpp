CBaseScene::~CBaseScene()
{

}

//////////////////////////////////////////////////////////////////////////

void CBaseScene::EnterBase()
{
	m_IsShown = true;

	Enter();
}

//////////////////////////////////////////////////////////////////////////

void CBaseScene::ExitBase()
{
	m_IsShown = false;

	Exit();
}

//////////////////////////////////////////////////////////////////////////

void CBaseScene::DrawBase()
{
	if (!m_IsShown)
	{
		return;
	}

	Draw();
}

//////////////////////////////////////////////////////////////////////////

bool CBaseScene::IsShown() const
{
	return m_IsShown;
}

